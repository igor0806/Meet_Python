# 3. Написати функцiю season, яка приймає один аргумент — номер мiсяця (вiд 1 до 12),
# яка буде повертати пору року, якiй цей мiсяць належить (зима, весна, лiто або осiнь)

def season (m):
    if  3 <= m <= 5:
        return ('весна')
    elif 6 <= m <= 8:
        return ('лето')
    elif 9 <= m <= 11:
        return ('осень')
    elif m == 12 or m == 1 or m == 2:
        return ('зима')
    else:
        return ('Такого месяца нет.')

print (season (int(input('Введите номер месяца года: '))))